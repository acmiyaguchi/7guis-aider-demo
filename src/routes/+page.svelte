<section>
  <h2>About 7GUI Tasks</h2>
  <p>The 7GUI tasks are a set of common GUI programming challenges designed to test the expressiveness and capabilities of UI frameworks. These tasks provide a benchmark for comparing different frameworks and their ability to handle typical user interface scenarios.</p>
  <p>Each task represents a typical UI problem, such as a counter, temperature converter, or a simple CRUD application. Implementing these tasks helps developers understand the strengths and weaknesses of a framework in handling real-world UI challenges.</p>
  <p>For more information, visit the original <a href="https://eugenkiss.github.io/7guis/">7GUI project page</a>.</p>
</section>

